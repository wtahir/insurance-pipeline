# stage6_evaluation.py
# Input: data/output/query_log.json (from Stage 5)
# Output: data/output/evaluation_report.json
#         data/output/evaluation_summary.json
#
# Uses GPT-4o as judge to evaluate both retrieval quality and answer quality.
# Distinguishes between retrieval failures and generation failures.
# This is the stage that tells you HOW to improve the pipeline.

import os
import json
import logging
from datetime import datetime
from openai import AzureOpenAI
from dotenv import load_dotenv

load_dotenv()

os.makedirs("logs", exist_ok=True)

logging.basicConfig(
    filename="logs/evaluation.log",
    level=logging.INFO,
    format="%(asctime)s - %(levelname)s - %(message)s"
)

client = AzureOpenAI(
    api_key=os.getenv("AZURE_OPENAI_API_KEY"),
    azure_endpoint=os.getenv("AZURE_OPENAI_ENDPOINT"),
    api_version="2024-08-01-preview"
)
DEPLOYMENT_NAME = os.getenv("AZURE_OPENAI_DEPLOYMENT", "gpt-4o")

QUERY_LOG_PATH = "data/output/query_log.json"

# Distance threshold — chunks above this are considered poor retrievals.
# Based on what we saw in Stage 5: 0.6 is a reasonable cutoff.
DISTANCE_THRESHOLD = 0.6

# --- Evaluation prompt ---
# We ask GPT-4o to score two things separately:
# 1. Retrieval quality — were the chunks relevant to the question?
# 2. Answer quality — was the answer accurate, complete, grounded in context?
# Scores are 1-5 with explicit criteria so the judge is consistent.

EVALUATION_PROMPT = """You are evaluating a RAG (Retrieval Augmented Generation) pipeline for insurance claims.

You will receive:
- A question asked by a user
- The context chunks retrieved from the database
- The answer generated by the LLM

Score the following on a scale of 1-5:

RETRIEVAL SCORE (were the right chunks retrieved?):
5 - All chunks are highly relevant, directly address the question
4 - Most chunks relevant, minor irrelevant content
3 - Mixed relevance, some useful chunks but also noise
2 - Mostly irrelevant chunks, little useful content retrieved
1 - Completely irrelevant chunks, wrong documents retrieved

ANSWER SCORE (was the answer good?):
5 - Complete, accurate, well-structured, directly answers the question
4 - Mostly complete and accurate, minor gaps
3 - Partially answers the question, some gaps or vagueness
2 - Incomplete or partially incorrect based on the context
1 - Wrong, hallucinated, or refuses to answer despite relevant context

FAILURE TYPE:
- "none" if both scores are 3 or above
- "retrieval" if retrieval score is below 3 (wrong chunks returned)
- "generation" if retrieval score is 3+ but answer score is below 3 (good chunks, bad answer)
- "both" if both scores are below 3

RESPOND ONLY with valid JSON, no explanation, no markdown:
{
  "retrieval_score": <1-5>,
  "answer_score": <1-5>,
  "failure_type": "<none|retrieval|generation|both>",
  "retrieval_notes": "<one sentence on retrieval quality>",
  "answer_notes": "<one sentence on answer quality>",
  "improvement_suggestion": "<one concrete suggestion to improve this specific query>"
}

Question: {query}

Retrieved chunks:
{context}

Generated answer:
{answer}"""


def format_chunks_for_evaluation(chunks: list[dict]) -> str:
    """
    Formats chunks for the evaluation prompt.
    Includes distance so the judge can factor in retrieval confidence.
    Truncates each chunk to avoid blowing the token budget.
    """
    parts = []
    for i, chunk in enumerate(chunks):
        meta = chunk.get("metadata", {})
        text = chunk.get("text", "")[:400]  # truncate long chunks
        distance = chunk.get("distance", "unknown")
        parts.append(
            f"[Chunk {i+1} | Distance: {distance} | "
            f"Claim: {meta.get('claim_number')} | "
            f"File: {meta.get('file_name')}]\n{text}"
        )
    return "\n\n".join(parts)


def evaluate_query(query_record: dict) -> dict:
    """
    Evaluates a single query record using GPT-4o as judge.
    Returns the record with evaluation fields populated.
    """
    query = query_record.get("query", "")
    chunks = query_record.get("chunks", [])
    answer = query_record.get("answer", "")

    # Calculate average distance — retrieval health indicator
    distances = [c.get("distance", 1.0) for c in chunks]
    avg_distance = round(sum(distances) / len(distances), 3) if distances else 1.0
    poor_chunks = sum(1 for d in distances if d > DISTANCE_THRESHOLD)

    context = format_chunks_for_evaluation(chunks)

    prompt = (
        EVALUATION_PROMPT
        + f"\n\nQuestion: {query}"
        + f"\n\nRetrieved chunks:\n{context}"
        + f"\n\nGenerated answer:\n{answer}"
    )

    try:
        response = client.chat.completions.create(
            model=DEPLOYMENT_NAME,
            messages=[{"role": "user", "content": prompt}],
            temperature=0,
            max_tokens=1000
        )

        raw = response.choices[0].message.content.strip()

        # Strip markdown fences if present
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]

        evaluation = json.loads(raw)

        # Merge evaluation results back into the query record
        query_record.update({
            "evaluated": True,
            "evaluated_at": datetime.now().isoformat(),
            "retrieval_score": evaluation.get("retrieval_score"),
            "answer_score": evaluation.get("answer_score"),
            "failure_type": evaluation.get("failure_type"),
            "retrieval_notes": evaluation.get("retrieval_notes"),
            "answer_notes": evaluation.get("answer_notes"),
            "improvement_suggestion": evaluation.get("improvement_suggestion"),
            "avg_distance": avg_distance,
            "poor_chunks_count": poor_chunks,
            "score": evaluation.get("answer_score")  # top-level score for quick sorting
        })

        logging.info(
            f"Evaluated: {query[:60]} | "
            f"retrieval={evaluation.get('retrieval_score')} "
            f"answer={evaluation.get('answer_score')} "
            f"failure={evaluation.get('failure_type')}"
        )

    except json.JSONDecodeError as e:
        logging.error(f"JSON parse failed for query '{query[:60]}': {e}")
        query_record.update({
            "evaluated": True,
            "evaluated_at": datetime.now().isoformat(),
            "retrieval_score": None,
            "answer_score": None,
            "failure_type": "evaluation_error",
            "retrieval_notes": f"Evaluation failed: {str(e)}",
            "answer_notes": None,
            "improvement_suggestion": None,
            "avg_distance": avg_distance,
            "poor_chunks_count": poor_chunks,
            "score": None
        })

    except Exception as e:
        logging.error(f"Unexpected error evaluating '{query[:60]}': {e}")
        query_record["evaluated"] = True
        query_record["failure_type"] = "evaluation_error"

    return query_record


def evaluate_all():
    if not os.path.exists(QUERY_LOG_PATH):
        raise FileNotFoundError("query_log.json not found. Run Stage 5 first.")

    with open(QUERY_LOG_PATH, "r", encoding="utf-8") as f:
        query_log = json.load(f)

    # Only evaluate queries not yet evaluated
    # Same idempotency pattern as Stage 2 and Stage 4
    pending = [q for q in query_log if not q.get("evaluated")]
    already_done = [q for q in query_log if q.get("evaluated")]

    print(f"Total queries: {len(query_log)}")
    print(f"Already evaluated: {len(already_done)}")
    print(f"Pending evaluation: {len(pending)}\n")

    if not pending:
        print("All queries already evaluated. Add new queries in Stage 5 and rerun.")
        return

    evaluated = []
    for i, record in enumerate(pending):
        print(f"[{i+1}/{len(pending)}] Evaluating: {record['query'][:60]}...")
        result = evaluate_query(record)
        evaluated.append(result)

        status = f"retrieval={result.get('retrieval_score')} answer={result.get('answer_score')} failure={result.get('failure_type')}"
        print(f"  → {status}")

    # Combine already evaluated + newly evaluated
    all_evaluated = already_done + evaluated

    # Save updated query log
    with open(QUERY_LOG_PATH, "w", encoding="utf-8") as f:
        json.dump(all_evaluated, f, indent=2, ensure_ascii=False)

    # Build evaluation report — sorted by answer score ascending
    # Worst performing queries first so you know what to fix first
    report = sorted(
        all_evaluated,
        key=lambda x: (x.get("answer_score") or 0)
    )

    with open("data/output/evaluation_report.json", "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, ensure_ascii=False)

    # Summary statistics
    scored = [q for q in all_evaluated if q.get("answer_score") is not None]
    failure_types = {}
    for q in scored:
        ft = q.get("failure_type", "unknown")
        failure_types[ft] = failure_types.get(ft, 0) + 1

    avg_retrieval = round(sum(q.get("retrieval_score", 0) for q in scored) / len(scored), 2) if scored else 0
    avg_answer = round(sum(q.get("answer_score", 0) for q in scored) / len(scored), 2) if scored else 0

    summary = {
        "run_at": datetime.now().isoformat(),
        "total_queries_evaluated": len(scored),
        "avg_retrieval_score": avg_retrieval,
        "avg_answer_score": avg_answer,
        "failure_type_breakdown": failure_types,
        "queries_needing_attention": [
            {
                "query": q.get("query"),
                "retrieval_score": q.get("retrieval_score"),
                "answer_score": q.get("answer_score"),
                "failure_type": q.get("failure_type"),
                "improvement_suggestion": q.get("improvement_suggestion"),
                "avg_distance": q.get("avg_distance")
            }
            for q in report if (q.get("answer_score") or 5) < 4
        ]
    }

    with open("data/output/evaluation_summary.json", "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2, ensure_ascii=False)

    logging.info(f"Evaluation complete. avg_retrieval={avg_retrieval} avg_answer={avg_answer}")
    print(f"\nDone.")
    print(f"Avg retrieval score: {avg_retrieval}/5")
    print(f"Avg answer score: {avg_answer}/5")
    print(f"Failure breakdown: {failure_types}")
    print(f"Queries needing attention: {len(summary['queries_needing_attention'])}")


if __name__ == "__main__":
    evaluate_all()